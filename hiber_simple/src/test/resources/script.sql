CREATE TABLE POST (
  POST_ID   INTEGER AUTO_INCREMENT PRIMARY KEY,
  TITLE     VARCHAR(100),
  POST_DATE TIMESTAMP
);

CREATE TABLE RECORD (
  ID      INTEGER AUTO_INCREMENT PRIMARY KEY,
  VALUE   VARCHAR,
  POST_ID INTEGER
);

ALTER TABLE RECORD ADD CONSTRAINT post_FK FOREIGN KEY (POST_ID) REFERENCES POST;

CREATE TABLE LIKE_ (
  ID  INTEGER AUTO_INCREMENT PRIMARY KEY,
  val CHAR(1)
);

CREATE TABLE RECORD_LIKE (
  LIKE_ID    INTEGER,
  RECORD__ID INTEGER
);

ALTER TABLE RECORD_LIKE ADD CONSTRAINT like_FK FOREIGN KEY (LIKE_ID) REFERENCES LIKE_;
ALTER TABLE RECORD_LIKE ADD CONSTRAINT rec_FK FOREIGN KEY (RECORD__ID) REFERENCES RECORD;

