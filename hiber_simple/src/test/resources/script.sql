  POST_ID   INTEGER AUTO_INCREMENT PRIMARY KEY ,
  TITLE     VARCHAR(100),
  POST_DATE TIMESTAMP
);

CREATE TABLE RECORD (
  ID   INTEGER AUTO_INCREMENT PRIMARY KEY ,
  VALUE VARCHAR,
  POST_ID INTEGER
);

alter table RECORD add constraint post_FK foreign key (POST_ID) references POST;

